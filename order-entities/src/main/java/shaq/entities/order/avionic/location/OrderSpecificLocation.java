package shaq.entities.order.avionic.location;

public class OrderSpecificLocation {

    private Long lon;
    private Long lat;
    private Long alt;

    public Long getLon() {
        return lon;
    }

    public void setLon(Long lon) {
        this.lon = lon;
    }

    public Long getLat() {
        return lat;
    }

    public void setLat(Long lat) {
        this.lat = lat;
    }

    public Long getAlt() {
        return alt;
    }

    public void setAlt(Long alt) {
        this.alt = alt;
    }
}
